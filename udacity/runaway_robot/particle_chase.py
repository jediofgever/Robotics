dist = lambda x1, y1, x2, y2: sqrt((x1 - x2) ** 2.0 + (y1 - y2) ** 2.0)

def get_position(particles):
    x = 0.0
    y = 0.0
    for p in particles:
        x += p.x
        y += p.y
    return [x / len(particles), y / len(particles)]

def measurement_prob(p, measurements):
    # calculate the correct measurement
    predicted_measurements = [p.x, p.y]

    # compute errors
    prob = 1.0
    for estimate, predict in zip(measurements, predicted_measurements):
        error = estimate - predict

        prob *= (exp(- (error ** 2) / (p.measurement_noise ** 2) / 2.0) /
                  sqrt(2.0 * pi * (p.measurement_noise ** 2)))
    return prob

def estimate_next_pos(measurement, OTHER = None):
    N = 1000
    if not OTHER:
        x, y = measurement
        particles = []
        for i in range(N):
            r = robot(random.gauss(x, 1), random.gauss(y, 1), random.random() * 2.0 * pi)
            r.set_noise(0.1, 0.1, 0.25)
            particles.append(r)
        OTHER = [measurement, 0.0, particles]
        xy_estimate = measurement
    else:
        prev_x, prev_angle, particles = OTHER

        # measurement update
        w = [measurement_prob(p, measurement) for p in particles]

        # resampling
        new_p = []
        index = int(random.random() * N)
        beta = 0.0
        mw = max(w)
        for i in range(N):
            beta += random.random() * 2.0 * mw
            while beta > w[index]:
                beta -= w[index]
                index = (index + 1) % N
            new_p.append(deepcopy(particles[index]))
        estimate = get_position(new_p)

        x, y = estimate
        diffX = x - prev_x[0]
        diffY = y - prev_x[1]
        angle = atan2(diffY, diffX)
        turn_rate = angle_trunc(angle - prev_angle)
        step = dist(x, y, prev_x[0], prev_x[1])

        # motion update (prediction)
        for p in new_p:
            p.move(turn_rate, step)

        future_p = []
        for p in new_p:
            future_p.append(deepcopy(p))
            future_p[len(future_p)-1].move(turn_rate, step)

        xy_estimate = get_position(future_p)
        OTHER = [estimate, angle, new_p]
    return xy_estimate, OTHER

def next_move(hunter_position, hunter_heading, target_measurement, max_distance, OTHER = None):
    # This function will be called after each time the target moves.
    tpid = [0.8, 0.15, 0.00]
    dpid = [0.5, 0.15, 0.00]

    if not OTHER:
       angle_prev = 0.0
       dist_prev = 0.0
       dist_error = 0.0
       angle_error = 0.0
       estimate_OTHER = None
    else:
       angle_error, dist_error, angle_prev, dist_prev, estimate_OTHER= OTHER

    future_position, estimate_OTHER = estimate_next_pos(target_measurement, estimate_OTHER)

    #intercept_heading = angle_trunc(get_heading(hunter_position, target_measurement) - hunter_heading)
    intercept_heading = angle_trunc(get_heading(hunter_position, future_position) - hunter_heading)
    intercept_distance = dist(hunter_position[0], hunter_position[1], future_position[0], future_position[1])

    dist_error += intercept_distance
    angle_error += intercept_heading
    angle_delta = intercept_heading - angle_prev
    dist_delta = intercept_distance - dist_prev

    turning = angle_trunc(tpid[0] * intercept_heading + tpid[1] * angle_delta + tpid[2] * angle_error)
    distance = min(dpid[0] * intercept_distance + dpid[1] * dist_delta + dpid[2] * dist_error, max_distance)

    # The OTHER variable is a place for you to store any historical information about
    # the progress of the hunt (or maybe some localization information). Your return format
    # must be as follows in order to be graded properly.
    OTHER = angle_error, dist_error, intercept_heading, intercept_distance, estimate_OTHER
    return turning, distance, OTHER

def angle_trunc(a):
    """This maps all angles to a domain of [-pi, pi]"""
    while a < 0.0:
        a += pi * 2
    return ((a + pi) % (pi * 2)) - pi

def get_heading(hunter_position, target_position):
    """Returns the angle, in radians, between the target and hunter positions"""
    hunter_x, hunter_y = hunter_position
    target_x, target_y = target_position
    heading = atan2(target_y - hunter_y, target_x - hunter_x)
    heading = angle_trunc(heading)
    return heading

def distance_between(point1, point2):
    """Computes distance between point1 and point2. Points are (x, y) pairs."""
    x1, y1 = point1
    x2, y2 = point2
    return sqrt((x2 - x1) ** 2 + (y2 - y1) ** 2)

